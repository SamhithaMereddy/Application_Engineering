    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = detailsTable.getSelectedRow();
        if (selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this,"Please select row to delete.");
            return;
        }
        DefaultTableModel model= (DefaultTableModel) detailsTable.getModel();
        Details selectedDetails = (Details) model.getValueAt(selectedRowIndex, 0);

        namejText.setText(selectedDetails.getName());
        idjText.setText(selectedDetails.getId());
        agejText.setText(String.valueOf(selectedDetails.getAge()));
        genderjText.setText(selectedDetails.getGender());
        datejText.setText(selectedDetails.getDate());
        leveljText.setText(selectedDetails.getLevel());
        tinfojText.setText(selectedDetails.getInfo());
        titlejText.setText(selectedDetails.getTitle());
        phonejText.setText(selectedDetails.getPhone());
        emailjText.setText(selectedDetails.getEmail());

    }//GEN-LAST:event_viewButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = detailsTable.getSelectedRow();
        if (selectedRowIndex<0) {
            
            JOptionPane.showMessageDialog(this,"Please select row to delete.");
            return;
        }
        
        DefaultTableModel model= (DefaultTableModel) detailsTable.getModel();
        Details selectedDetails = (Details) model.getValueAt(selectedRowIndex, 0);
        
        history.deleteDetails(selectedDetails);
        
        JOptionPane.showMessageDialog(this,"Details deleted.");
        populateTable();
    }//GEN-LAST:event_deleteButtonActionPerformed

