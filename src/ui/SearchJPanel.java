    private void populateTable() {
        
        DefaultTableModel model= (DefaultTableModel) detailsTable.getModel();
        model.setRowCount(0);
        
        for (Details ds : history.getHistory()) {
            Object[] row = new Object[10];
            row[0] = ds;
            row[1] = ds.getId();
            row[2] = ds.getAge();
            row[3] = ds.getGender();
            row[4] = ds.getDate();
            row[5] = ds.getLevel();
            row[6] = ds.getInfo();
            row[7] = ds.getTitle();
            row[8] = ds.getPhone();
            row[9] = ds.getEmail();
            
            model.addRow(row);
            
        }
    }
